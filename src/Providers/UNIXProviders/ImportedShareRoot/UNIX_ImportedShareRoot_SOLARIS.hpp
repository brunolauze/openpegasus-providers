UNIX_ImportedShareRoot::UNIX_ImportedShareRoot(void)
{
}

UNIX_ImportedShareRoot::~UNIX_ImportedShareRoot(void)
{
}

Boolean UNIX_ImportedShareRoot::getAntecedent(CIMProperty &p) const
{
	p = CIMProperty(PROPERTY_ANTECEDENT, getAntecedent());
	return true;
}

CIMInstance UNIX_ImportedShareRoot::getAntecedent() const
{
	return _antecedent;
}

void UNIX_ImportedShareRoot::setAntecedent(CIMInstance &value)
{
	_antecedent = value;
}

Boolean UNIX_ImportedShareRoot::getDependent(CIMProperty &p) const
{
	p = CIMProperty(PROPERTY_DEPENDENT, getDependent());
	return true;
}

CIMInstance UNIX_ImportedShareRoot::getDependent() const
{
	return _dependent;
}

void UNIX_ImportedShareRoot::setDependent(CIMInstance &value)
{
	_dependent = value;
}


void UNIX_ImportedShareRoot::clearInstance()
{

	_antecedent = _antecedentSqlSchemaProvider.constructInstance(
		CIMName("UNIX_SqlSchema"),
		CIMNamespaceName("root/cimv2"),
		_antecedentSqlSchema);
	_dependent = _dependentSqlSchemaProvider.constructInstance(
		CIMName("UNIX_SqlSchema"),
		CIMNamespaceName("root/cimv2"),
		_dependentSqlSchema);

}

Boolean UNIX_ImportedShareRoot::loadInstance(const CIMInstance &instance)
{
	clearInstance();
	Uint32 propertyCount = instance.getPropertyCount();
	for(Uint32 i = 0; i < propertyCount; i++) {
		CIMConstProperty property = instance.getProperty(i);
			if (String::equal(property.getName().getString(), "Antecedent"))
			{
				CIMInstance antecedentValue;
				property.getValue().get(antecedentValue);
				setAntecedent(antecedentValue);
			}
			else if (String::equal(property.getName().getString(), "Dependent"))
			{
				CIMInstance dependentValue;
				property.getValue().get(dependentValue);
				setDependent(dependentValue);
			}
	}
	return true;
}


